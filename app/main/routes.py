from langdetect import LangDetectException, detect


#THIS DECORATOR EXECS BEFORE VIEW BASICALLY LOGS TIME WHEN USER REQUESTS SITE
@bp.before_app_request 
def before_request():
    # IF CURRENT USER IS LOGGED IN, LOG TIME TO LAST SEEN.
    if current_user.is_authenticated:
        current_user.last_seen = datetime.utcnow()
        db.session.commit()
    g.locale = str(get_locale())


# THE INDEX/DEFAULT PAGE ROUTE
@bp.route('/', methods = ['GET','POST'])
@bp.route('/index', methods = ['GET','POST'])
# NOT SHOWN UNLESS LOGGED IN OTHERWISE REDIRECTED TO LOGIN.HTML
@login_required
def index():
# TRIAL NAMES AND POSTS
    form = PostForm()
    if form.validate_on_submit():
        try:
            language = detect(form.post.data)
        except LangDetectException:
            language = ''
        post = Post(body=form.post.data, author=current_user,
                    language=language)
        db.session.add(post)
        db.session.commit()
        flash(_('Your post is now live!'))
        return redirect(url_for('main.index'))
    page = request.args.get('page', 1, type=int)
    posts = current_user.followed_posts().paginate(
        page, current_app.config['POSTS_PER_PAGE'], False)
    next_url = url_for('main.index', page=posts.next_num) \
        if posts.has_next else None
    prev_url = url_for('main.index', page=posts.prev_num) \
        if posts.has_prev else None
    return render_template('index.html', title=_('Home'), form=form,
                           posts=posts.items, next_url=next_url,
                           prev_url=prev_url)


# USER'S PROFILE PAGE ROUTER
@bp.route('/user/<username>')
@login_required
# REQUIRES OWN LOGIN, RETURNS OWN USER PROFILE OR 404 NOT FOUND
def user(username):
    user = User.query.filter_by(username=username).first_or_404()
    page = request.args.get('page', 1, type=int)
    posts = user.posts.order_by(Post.timestamp.desc()).paginate(
        page, current_app.config['POSTS_PER_PAGE'], False)
    next_url = url_for('main.user', username=user.username, page=posts.next_num) \
        if posts.has_next else None
    prev_url = url_for('main.user', username=user.username, page=posts.prev_num) \
        if posts.has_prev else None
    form = EmptyForm()
    return render_template('user.html', user=user, posts=posts.items,
                           next_url=next_url, prev_url=prev_url, form=form)


# EDIT PROFILE ROUTER
@bp.route('/edit_profile', methods=['GET', 'POST'])
@login_required
def edit_profile():
    # LINKS THE HTML FOR EDITING PROFILE PAGE
    form = EditProfileForm(current_user.username)
    # IF POST, THE CHANGES ONLY COMMIT UNLESS FORM DATA INVALID
    if form.validate_on_submit():
        current_user.username = form.username.data
        current_user.about_me = form.about_me.data
        db.session.commit()
        flash(_('Your changes have been saved.'))
        return redirect(url_for('main.user', username = current_user.username))
    # THE GET FETCHES THE CURRENT BIO AND USERNAME SO THE USER CAN EDIT FROM THE ORIGINIAL
    elif request.method == 'GET':
        form.username.data = current_user.username
        form.about_me.data = current_user.about_me
    return render_template('edit_profile.html', title=_('Edit Profile'),
                           form=form)


@bp.route('/follow/<username>', methods=['POST'])
@login_required
def follow(username):
    form = EmptyForm()
    if form.validate_on_submit():
        user = User.query.filter_by(username=username).first()
        if user is None:
            flash(_('User %(username)s not found.', username=username))
            return redirect(url_for('main.index'))
        if user == current_user:
            flash(_('You cannot follow yourself!'))
            return redirect(url_for('main.user', username=username))
        current_user.follow(user)
        db.session.commit()
        flash(_('You are following %(username)s!', username=username))
        return redirect(url_for('main.user', username=username))
    else:
        return redirect(url_for('main.index'))


